import { readFileSync, writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'
import { defaultAdapters } from '../src/adapters/registry.ts'
import { resolveAdapterEntry } from '../src/adapters/contract.ts'

const __dirname = dirname(fileURLToPath(import.meta.url))
const readmePath = join(__dirname, '../README.md')
const START_MARKER = '<!-- CAPABILITIES_TABLE:START (generated by scripts/generate-capabilities-table.mjs — do not edit by hand) -->'
const END_MARKER = '<!-- CAPABILITIES_TABLE:END -->'

function yesNo(value) {
  return value ? 'Yes' : '-'
}

export function buildCapabilitiesTable() {
  const rows = Object.entries(defaultAdapters).map(([name, entry]) => {
    const { capabilities } = resolveAdapterEntry(entry)
    return `| **${name}** | ${yesNo(capabilities.structuredOutput)} | ${yesNo(capabilities.images)} | ${yesNo(capabilities.sandbox)} | ${yesNo(capabilities.skipPermissions)} |`
  })

  return [
    START_MARKER,
    '| Provider CLI | Structured Output | Image Analysis | Sandboxed Mode | Skip Permissions |',
    '| :--- | :---: | :---: | :---: | :---: |',
    ...rows,
    END_MARKER,
  ].join('\n')
}

export function readReadmeBlock() {
  const readme = readFileSync(readmePath, 'utf8')
  const start = readme.indexOf(START_MARKER)
  const end = readme.indexOf(END_MARKER)
  if (start === -1 || end === -1) {
    throw new Error(`Capabilities table markers not found in ${readmePath}`)
  }
  return readme.slice(start, end + END_MARKER.length)
}

function writeReadmeBlock(table) {
  const readme = readFileSync(readmePath, 'utf8')
  const start = readme.indexOf(START_MARKER)
  const end = readme.indexOf(END_MARKER)
  if (start === -1 || end === -1) {
    throw new Error(`Capabilities table markers not found in ${readmePath}`)
  }
  const updated = readme.slice(0, start) + table + readme.slice(end + END_MARKER.length)
  writeFileSync(readmePath, updated)
}

if (import.meta.url === `file://${process.argv[1]}`) {
  const table = buildCapabilitiesTable()
  if (process.argv.includes('--write')) {
    writeReadmeBlock(table)
    console.log('README.md capabilities table updated.')
  } else {
    console.log(table)
  }
}
